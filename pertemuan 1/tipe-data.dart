void main(){

  // Variabel
  var namavariabel = 10;

  print(namavariabel);

  // String
  var bahasa = 'dart';
  var bahasa1 = "dart";

  print(bahasa);
  print(bahasa1);

  // Number (int)
  int angka = 10;
  double desimal = 10;

  print(angka);
  print(desimal);

  // Boolean (bool)
  bool asli = true;
  bool palsu = false;

  print(asli);
  print(palsu);

  // Dynamic
  dynamic fleksibel = 10;
  dynamic fleksibel2 = 'kalimat';
  dynamic fleksibel3 = 10.0;
  dynamic fleksibel4 = true;

  print(fleksibel);
  print(fleksibel2);
  print(fleksibel3);
  print(fleksibel4);

  // Operator (+, -, *, /, %)

  var nilaipertama = 4;
  var nilaikedua = 10;

  var tambah = nilaipertama + nilaikedua;
  var tambah2 = (4+10);
  print(tambah);


  print(5+5);
  print(5-5);
  print(5*5);
  print(5/5);
  print(5%5);

  print(4+2*10);

  // Equal ( == )
  var seratus = 100;
  print(seratus==5);
  print(seratus==100);

  // Not Equal ( != )
  var sifat = 'rajin';

  print(sifat != 'malas');

  // Kurang lebih dari (<, >, <=, >=)
  print(seratus < 20);
  print(seratus > 20);
  print(seratus >= 100);
  print(seratus <= 102);


  // Or ( || )
  print('--------OR-------');
  print(true || true);
  print(true || false);
  print(true || false || false);
  print(false || false);

  // And (&&)
  print('------AND------');
  print(true && true);
  print(true && false);
  print(false && false);
  print(true && false && true);





}

